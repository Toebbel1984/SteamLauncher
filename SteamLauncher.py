def _split_registry_root(key: str) -> tuple[int, str]:
    root_text, separator, subkey = key.partition("\\")
    roots = {
        "HKLM": winreg.HKEY_LOCAL_MACHINE,
        "HKEY_LOCAL_MACHINE": winreg.HKEY_LOCAL_MACHINE,
        "HKCU": winreg.HKEY_CURRENT_USER,
        "HKEY_CURRENT_USER": winreg.HKEY_CURRENT_USER,
        "HKCR": winreg.HKEY_CLASSES_ROOT,
        "HKEY_CLASSES_ROOT": winreg.HKEY_CLASSES_ROOT,
        "HKU": winreg.HKEY_USERS,
        "HKEY_USERS": winreg.HKEY_USERS,
        "HKCC": winreg.HKEY_CURRENT_CONFIG,
        "HKEY_CURRENT_CONFIG": winreg.HKEY_CURRENT_CONFIG,
    }
    try:
        return roots[root_text.upper()], subkey if separator else ""
    except KeyError as exc:
        raise ValueError(f"Unbekannter Registry-Root: {root_text}") from exc


def _reg_key_exists(key: str) -> bool:
    try:
        root, subkey = _split_registry_root(key)
        handle = winreg.OpenKey(root, subkey, 0, winreg.KEY_READ | REGISTRY_VIEW)
    except (OSError, ValueError):
        return False
    else:
        handle.Close()
        return True


def _enum_registry_subkeys(key: str) -> Iterator[str]:
    root, subkey = _split_registry_root(key)
    try:
        with winreg.OpenKey(root, subkey, 0, winreg.KEY_READ | REGISTRY_VIEW) as handle:
            index = 0
            while True:
                try:
                    yield winreg.EnumKey(handle, index)
                except OSError:
                    break
                index += 1
    except OSError:
        return


def _read_registry_value(key: str, value_name: str) -> tuple[bool, object | None]:
    try:
        root, subkey = _split_registry_root(key)
        with winreg.OpenKey(root, subkey, 0, winreg.KEY_READ | REGISTRY_VIEW) as handle:
            value, _ = winreg.QueryValueEx(handle, value_name)
        return True, value
    except (OSError, ValueError):
        return False, None


def _write_registry_dword(key: str, value_name: str, value: int) -> bool:
    try:
        root, subkey = _split_registry_root(key)
        with winreg.CreateKeyEx(root, subkey, 0, winreg.KEY_SET_VALUE | REGISTRY_VIEW) as handle:
            winreg.SetValueEx(handle, value_name, 0, winreg.REG_DWORD, int(value))
        return True
    except (OSError, ValueError):
        return False


def _delete_registry_value(key: str, value_name: str) -> bool:
    try:
        root, subkey = _split_registry_root(key)
        with winreg.OpenKey(root, subkey, 0, winreg.KEY_SET_VALUE | REGISTRY_VIEW) as handle:
            winreg.DeleteValue(handle, value_name)
        return True
    except (OSError, ValueError):
        return False


def _reg_write_dword_if_needed(
    key: str,
    value_name: str,
    value: int,
    create_missing_key: bool,
) -> int:
    # Umgekehrte Existenzprüfung des Originals unverändert übernommen.
    if create_missing_key and _reg_key_exists(key):
        return 0

    exists, current = _read_registry_value(key, value_name)
    if exists:
        try:
            if int(current) == int(value):
                return 0
        except (TypeError, ValueError):
            pass
    return int(_write_registry_dword(key, value_name, value))


def _reg_delete_value_if_exists(key: str, value_name: str) -> int:
    exists, _ = _read_registry_value(key, value_name)
    if not exists:
        return 0
    return int(_delete_registry_value(key, value_name))


