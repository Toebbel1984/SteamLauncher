def _ensure_admin() -> None:
    if shell32.IsUserAnAdmin():
        return

    if getattr(sys, "frozen", False):
        executable = sys.executable
        params = subprocess.list2cmdline(sys.argv[1:])
    else:
        executable = sys.executable
        params = subprocess.list2cmdline([str(Path(__file__).resolve()), *sys.argv[1:]])

    result = shell32.ShellExecuteW(
        None,
        "runas",
        executable,
        params or None,
        str(SCRIPT_DIR),
        SW_HIDE,
    )
    value = ctypes.cast(result, ctypes.c_void_p).value or 0
    if value <= 32:
        raise SystemExit(f"Administratorrechte konnten nicht angefordert werden (Fehler {value}).")
    raise SystemExit(0)


